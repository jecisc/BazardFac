package drawing;

/**
 *  Drawing Exception
 *
 *@author     Anne-Cecile Caron
 */
public class DrawingException extends Exception {
  /**
   *  Constructor for the DrawingException object
   */
  public DrawingException() {
    super();
  }


  /**
   *  Constructor for the DrawingException object
   *
   *@param  msg  error message
   */
  public DrawingException(String msg) {
    super(msg);
  }

}

